    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name_text = name.getText();
        String garanty_text = (String) garanties.getSelectedItem();
        int garanty = Integer.parseInt(garanty_text);
        int price_number = Integer.parseInt(price.getText());
        int result = Database.get_instance().add_service(name_text, garanty, price_number);
        switch (result) {
            case 0 -> JOptionPane.showMessageDialog(null, "Service added Succesfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            case 1 -> JOptionPane.showMessageDialog(null, "Database Error, Service not Added", "Error", JOptionPane.ERROR_MESSAGE);
            default -> JOptionPane.showMessageDialog(null, "Unknown Error, Service not Added", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

